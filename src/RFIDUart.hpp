#pragma once

#include <Arduino.h>

class RFIDUart {
public:
    RFIDUart();
    void init();
    void update();
    void send(const char* data);
    String receive();
    bool isAvailable();
    void clear();
    void end();
};

// ----------------------------------------------------------------------------------------------------------------------------------
// ==================================================================================================================================
// 処理の実装部分
// ==================================================================================================================================
// ----------------------------------------------------------------------------------------------------------------------------------

RFIDUart::RFIDUart() {
}

// シリアル通信の初期化
void RFIDUart::init() {
    // 2ピンをRX(受信), 1ピンをTX(送信)にする
    Serial2.begin(115200, SERIAL_8N1, 2, 1);
}